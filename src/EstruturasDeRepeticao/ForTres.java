package EstruturasDeRepeticao;

public class ForTres {


    public static void main(String[] args) {

        int i = 0;

        for (; i <= 1000; i++) {
            for (int x = 0; x <= 1000; x++){

                System.out.printf("[%d  %d] ", i, x );

            }
        System.out.println("");
        }


    }
}
